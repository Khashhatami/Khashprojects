Title: Cleans up text and perfrom basic text analysis 
Author: "Khashayar Hatami <https://github.com/Khashhatami/Khashprojects> [aut, cre]"
Description: This package accepts txt files, and after cleaning them up 
(removing stop words, punctucations, etc), conducts basic semantic analytics on them. 
These analytics include three functions : 1. creating a word cloud to highlight the most important words
2.Utilizing lexicon_bing dictionary to give a ration of negative and positive words to the user as well 
as a char that shows that ratio 
